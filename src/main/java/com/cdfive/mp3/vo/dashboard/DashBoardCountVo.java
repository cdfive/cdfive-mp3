package com.cdfive.mp3.vo.dashboard;

public class DashBoardCountVo {
	private Integer mp3Count;
	private Integer catalogCount;
	private Integer articleCount;
	private Integer userCount;

	public Integer getMp3Count() {
		return mp3Count;
	}

	public void setMp3Count(Integer mp3Count) {
		this.mp3Count = mp3Count;
	}

	public Integer getCatalogCount() {
		return catalogCount;
	}

	public void setCatalogCount(Integer catalogCount) {
		this.catalogCount = catalogCount;
	}

	public Integer getArticleCount() {
		return articleCount;
	}

	public void setArticleCount(Integer articleCount) {
		this.articleCount = articleCount;
	}

	public Integer getUserCount() {
		return userCount;
	}

	public void setUserCount(Integer userCount) {
		this.userCount = userCount;
	}

}
